import java.util.*;

import java.lang.*;

import java.io.*;

class DownwardTrianglestar{
public static void main(String args[])throws java.lang.Exception{

					int i,j;
					Scanner sc=new Scanner(System.in);
					System.out.println("Enter number of rows that you want in pattern: ");
					int term=sc.nextInt();
					for(i=term;i>0;i--){
							     for(j=i;j>0;j--){
										System.out.print("*");
									      }
							      System.out.println();
							  }
							}
						    }




			